package _1_OOPS_Concept;

public class _1_Introduction {
	
//	Java is an object-oriented programming language, which means it is designed around the concept of objects. Object-oriented programming (OOP) is a programming paradigm that uses objects to represent and manipulate data. Here are some of the main concepts of OOP in Java:
//
//		Class: A class is a blueprint for creating objects. It defines the properties and methods that an object of that class will have.
//
//		Object: An object is an instance of a class. It is created using the new keyword followed by the name of the class.
//
//		Encapsulation: Encapsulation is the practice of hiding the implementation details of an object from the outside world. It is achieved in Java by using access modifiers such as private, protected, and public.
//
//		Inheritance: Inheritance is a mechanism in which one class can inherit properties and methods from another class. This allows for code reuse and can make programs easier to maintain.
//
//		Polymorphism: Polymorphism is the ability of an object to take on many forms. In Java, this is achieved through method overloading and method overriding.
//
//		Abstraction: Abstraction is the practice of focusing on the essential features of an object and ignoring the non-essential details. It is achieved in Java through abstract classes and interfaces.
//
//		Interface: An interface is a collection of abstract methods that can be implemented by any class. It defines a contract for what methods a class must implement.
//
//		Method: A method is a block of code that performs a specific task. It is defined within a class and can be called on an object of that class.
//
//		By using these OOP concepts in Java, developers can create programs that are modular, flexible, and easy to maintain.

}
